function addEntry(option) {
    const table = document.querySelector(`#${option}Fields table`);
    const headers = table.querySelectorAll("th");

    const newRow = table.insertRow();
    for (let i = 0; i < headers.length; i++) {
        const headerText = headers[i].innerText;
        newRow.innerHTML += `
            <td><input type="text" name="${option}_${headerText.toLowerCase()}_${table.rows.length}"></td>
        `;
    }
}